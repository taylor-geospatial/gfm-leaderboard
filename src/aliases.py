"""Alias helpers."""

import re
from typing import Optional

_PAREN = re.compile(r"\s*\([^)]*\)\s*")
_PUNCT_WS = re.compile(r"[^a-z0-9+]+")


def _slug(s: str) -> str:
    s = _PAREN.sub(" ", (s or "").lower())
    return _PUNCT_WS.sub("", s)


def _strip_paren(s: str) -> str:
    """Strip trailing parenthesized qualifiers: 'Sentinel-2 (SatlasPretrain)' -> 'Sentinel-2'."""
    return _PAREN.sub(" ", s or "").strip()


