_ALWAYS_EXCLUDE_SLUGS: set[str] = {_slug(s) for s in ALWAYS_EXCLUDE_DATASETS}
_DOWNSTREAM_EXCLUDE_SLUGS: set[str] = {_slug(s) for s in DOWNSTREAM_EXCLUDE_DATASETS}
_PRETRAIN_EXCLUDE_SLUGS: set[str] = {_slug(s) for s in PRETRAIN_EXCLUDE_DATASETS}


def norm_dataset(raw: str, context: str = "downstream") -> Optional[str]:
    """Return the canonical dataset label for ``raw`` or None.

    ``context="downstream"`` (default) excludes ALWAYS_EXCLUDE and
    DOWNSTREAM_EXCLUDE — appropriate when normalizing held-out benchmarks.
    ``context="pretrain"`` excludes ALWAYS_EXCLUDE and PRETRAIN_EXCLUDE —
    drops generic descriptors (``"custom Sentinel-2 pretraining dataset"``)
    that aren't *named* corpora, while keeping real pretraining sources
    (ImageNet-1K, MERRA-2, SatlasPretrain) that we exclude on the
    downstream side.
    """
    if not raw:
        return None
    key = _slug(raw)
    if not key:
        return None
    if key in _ALWAYS_EXCLUDE_SLUGS:
        return None
    if context == "downstream" and key in _DOWNSTREAM_EXCLUDE_SLUGS:
        return None
    if context == "pretrain" and key in _PRETRAIN_EXCLUDE_SLUGS:
        return None
    if key in DATASET_ALIASES:
        return DATASET_ALIASES[key]
    stripped = _strip_paren(raw)
    if stripped != raw:
        key2 = _slug(stripped)
        if key2 in _ALWAYS_EXCLUDE_SLUGS:
            return None
        if context == "downstream" and key2 in _DOWNSTREAM_EXCLUDE_SLUGS:
            return None
        if context == "pretrain" and key2 in _PRETRAIN_EXCLUDE_SLUGS:
            return None
        if key2 in DATASET_ALIASES:
            return DATASET_ALIASES[key2]
    return stripped or raw.strip()


def is_sensor_only(raw: str) -> bool:
    """True iff ``raw`` is a bare sensor / satellite stream label."""
    if not raw:
        return False
    if _slug(raw) in _SENSOR_SLUGS:
        return True
    stripped = _strip_paren(raw)
    return stripped != raw and _slug(stripped) in _SENSOR_SLUGS


def canonical_slug(raw: str) -> Optional[str]:
    """Slug of the canonical name (safe for alias-aware Counter keys)."""
    c = norm_dataset(raw)
    return _slug(c) if c else None
